    private void tabelMatkulMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMatkulMouseClicked
        // get row index yang di klik
        int row = tabelMatkul.getSelectedRow();
        
        // get value kolom pada row yang di klik
        String kode = tabelMatkul.getValueAt(row, 0).toString();
        String nama = tabelMatkul.getValueAt(row, 1).toString();
        String sks  = tabelMatkul.getValueAt(row, 2).toString();
        
        // set value pada textField kode, nama, sks
        textKodeMatkul.setText(kode);
        textNamaMatkul.setText(nama);
        textSKS.setText(sks);
        
    }//GEN-LAST:event_tabelMatkulMouseClicked

