    private void loadDataMatkul() throws SQLException {
        // get semua data matkul dari database
        ResultSet rs = this.model.getAllData();
        // set data matkul ke table matkul 
        tabelMatkul.setModel(this.model.matkulTabelModel(rs));
    }
    
